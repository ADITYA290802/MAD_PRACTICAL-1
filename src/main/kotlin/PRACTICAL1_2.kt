fun main(){
    val a:Int = 30;
    val b:Float = 2.5F
    val c:String = "ADITYA"
    val d:Boolean = true
    val e:Double = 65.65
    val f:Char = 'A'
    val g:Long = 20012011076
    val h:Short = -69
    val i:Byte = 125
    println("Integer Value : $a")
    println("Float Value : $b")
    println("string Value : $c")
    println("Boolean Value : $d")
    println("Double Value : $e")
    println("Character Value : $f")
    println("Long Value : $g")
    println("Short Value : $h")
    println("Byte Value : $i")
}